    private void obrirDirButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_obrirDirButtonActionPerformed
        try {
            // Obtener el objeto File de la carpeta
            File carpeta = new File(rutaCSVSeparats);

            // Verificar si la carpeta existe y es un directorio
            if (carpeta.exists() && carpeta.isDirectory()) {
                // Abrir la carpeta con el explorador de archivos predeterminado del sistema
                Desktop.getDesktop().open(carpeta);
            } else {
                System.out.println("La carpeta especificada no existeix.");
                infoTextArea.append("La carpeta especificada no existeix: " + carpeta +"\n");
            }
        } catch (IOException ex) {
            System.out.println("Error al obrir la carpeta: " + ex.getMessage());
            infoTextArea.append("Error al obrir la carpeta: " + ex.getMessage());
        }
    }//GEN-LAST:event_obrirDirButtonActionPerformed

    private void separarValoresRepetidos() {

        limpiarInfoTextArea();
        
        String columna = columTextField.getText().trim();
        String prefijo = prefixTextField.getText().trim();
        String archivoBase = rutaCSVSeparats + sep + "base.csv"; // Ruta del archivo base

        try {
            BufferedReader br = new BufferedReader(new FileReader(archivoBase));
            Map<String, List<String>> repetidos = new HashMap<>();

            String line;
            while ((line = br.readLine()) != null) {
                String[] data = line.split(";");
                if (data.length >= Integer.parseInt(columna)) {
                    String valor = data[Integer.parseInt(columna) - 1];
                    if (!repetidos.containsKey(valor)) {
                        repetidos.put(valor, new ArrayList<>());
                    }
                    repetidos.get(valor).add(line);
                }
            }
            br.close();

            File carpetaCSVSeparados = new File(rutaCSVSeparats);
            if (!carpetaCSVSeparados.exists()) {
                carpetaCSVSeparados.mkdirs();
            }

            for (String valor : repetidos.keySet()) {
                if (repetidos.get(valor).size() > 1) {
                    String outputFile = rutaCSVSeparats + sep + prefijo + valor + ".csv";
                    BufferedWriter bw = new BufferedWriter(new FileWriter(outputFile));

                    for (String row : repetidos.get(valor)) {
                        bw.write(row);
                        bw.newLine();
                    }
                    bw.close();
                    infoTextArea.append("CSV separat per el valor repetit '" + valor + "' a: " + outputFile + "\n");
                }
            }

            JOptionPane.showMessageDialog(this, "Valors repetits separats correctament.", "Info", JOptionPane.INFORMATION_MESSAGE);
        } catch (IOException | NumberFormatException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al processar els arxius.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
