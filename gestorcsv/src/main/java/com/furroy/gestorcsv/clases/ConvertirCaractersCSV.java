    private void btnConvertirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConvertirActionPerformed
        String ruta = txtRutaArxiu.getText();
        if (ruta.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Sisplau selecciona un arxiu.");
            return;
        }

        File arxiuEntrada = new File(ruta);
        File arxiuSortida = new File(ruta.replace(".csv", "_convertido.csv"));

        try (
            BufferedReader br = new BufferedReader(new FileReader(arxiuEntrada));
            BufferedWriter bw = new BufferedWriter(new FileWriter(arxiuSortida))
        ) {
            String linia;
            while ((linia = br.readLine()) != null) {
                String convertida = substituirComesForaDeCometes(linia);
                        
                bw.write(convertida);
                bw.newLine();
            }
            JOptionPane.showMessageDialog(this, "Arxiu convertit:\n" + arxiuSortida.getAbsolutePath());
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }

    }//GEN-LAST:event_btnConvertirActionPerformed

